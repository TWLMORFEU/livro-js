/*
----------PRIORIDADES---------

Parênteses ()

Sempre resolvem primeiro.

Funções do Math

Como Math.pow(), Math.sqrt(), etc.

Executadas com base na ordem do parênteses.

Exponenciação **

Multiplicação *, Divisão /, Resto %

Mesma prioridade. Avaliados da esquerda para a direita.

Adição + e Subtração -

Também com mesma prioridade, e da esquerda para a direita.

------ Funções matemáticas com Math ------

Math.sqrt(x)
Retorna a raiz quadrada de x.
Exemplo: Math.sqrt(9) → 3

Math.pow(base, expoente)
Retorna a potência: base elevada ao expoente.
Exemplo: Math.pow(2, 3) → 8
(2³ = 8)

Math.round(x)
Arredonda o número x para o inteiro mais próximo.
Exemplo: Math.round(4.6) → 5

Math.floor(x)
Arredonda para baixo (maior inteiro menor ou igual a x).
Exemplo: Math.floor(4.9) → 4

Math.ceil(x)
Arredonda para cima (menor inteiro maior ou igual a x).
Exemplo: Math.ceil(4.1) → 5

Math.random()
Retorna um número aleatório entre 0 (inclusive) e 1 (exclusivo).
Exemplo: Math.random() → pode dar algo como 0.72835

Math.abs(x)
Retorna o valor absoluto (positivo) de x.
Exemplo: Math.abs(-10) → 10

Math.max(a, b, c, ...)
Retorna o maior valor entre os argumentos fornecidos.
Exemplo: Math.max(2, 10, 5) → 10

Math.min(a, b, c, ...)
Retorna o menor valor entre os argumentos fornecidos.
Exemplo: Math.min(2, 10, 5) → 2

*/